	for i in range(len(data)):
		data[i] ^= 0x67
		data[i] = box2.index(data[i])

	new = [None] * len(data)
	for _ in range(7):
		for i in range(len(data)-1, -1, -1):
			new[i] = data[pbox.index(i)]
		data = new[:]

	for i in range(len(data)):
		data[i] = (data[i] - 1 + 256) % 256
		data[i] = box.index(data[i])
	
	return bytes(data[::-1])


