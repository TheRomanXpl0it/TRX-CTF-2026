#define STACK_SIZE 1024
#define MEM_SIZE (1 << 20)

typedef struct {
    uint32_t op;
    uint32_t arg;
} Instr;

typedef struct {
    Instr *code;
    size_t code_size;
    size_t ip;
    int32_t stack[STACK_SIZE];
    int sp;
    uint32_t rng_state;
    uint32_t mem[MEM_SIZE];
} VM;

typedef struct {
    uint32_t a;
    uint32_t b;
    uint32_t c;
} Rand_struct;

VM* vm_create(Instr *code, size_t size) {
    VM *vm = malloc(sizeof(VM));
    if (!vm) return NULL;
    memset(vm, 0, sizeof(VM));
    vm->code = code;
    vm->code_size = size;
    vm->sp = -1;
    vm->rng_state = (uint32_t)time(NULL) ^ (uint32_t)getpid() ^ 0xA5C3F1E9u;
    if (vm->rng_state == 0) {
        vm->rng_state = 1;
    }
    return vm;
}

void vm_free(VM *vm) {
    free(vm);
}

void vm_push(VM *vm, int32_t val) {
    if (vm->sp < STACK_SIZE - 1) {
        vm->stack[++vm->sp] = val;
    }
}

void vm_add(VM *vm) {
    if (vm->sp >= 1) {
        int32_t b = vm->stack[vm->sp--];
        int32_t a = vm->stack[vm->sp];
        vm->stack[vm->sp] = a + b;
    }
}

void vm_sub(VM *vm) {
    if (vm->sp >= 1) {
        int32_t b = vm->stack[vm->sp--];
        int32_t a = vm->stack[vm->sp];
        vm->stack[vm->sp] = a - b;
    }
}

void vm_mul(VM *vm) {
    if (vm->sp >= 1) {
        int32_t b = vm->stack[vm->sp--];
        int32_t a = vm->stack[vm->sp];
        vm->stack[vm->sp] = a * b;
    }
}

void vm_div(VM *vm) {
    if (vm->sp >= 1) {
        int32_t b = vm->stack[vm->sp--];
        int32_t a = vm->stack[vm->sp];
        if (b == 0) {
            vm->stack[vm->sp] = 0;
            return;
        }
        vm->stack[vm->sp] = a / b;
    }
}

uint32_t kinda_random_stuff1(uint32_t input) {
    Rand_struct rs;
    rs.a = (input >> 16) & 0xFFFFu;
    rs.b = (input >> 8)  & 0xFFu;
    rs.c =  input        & 0xFFu;

    uint32_t a = (rs.a << 1) | 1;
    uint32_t b = (rs.b << 2) | 2;
    uint32_t c = rs.c * 4 + (rs.a << 4);
    uint32_t d = rs.c ^ rs.b;

    uint64_t t = (uint64_t)input * (uint64_t)a + (uint64_t)c;
    t += (((uint64_t)input * (uint64_t)b) + (uint64_t)d) << 32;

    return (uint32_t)t ^ (uint32_t)(t >> 32);
}

uint32_t invert_xor_right(uint32_t y, unsigned shift) {
    uint32_t x = y;
    for (unsigned i = shift; i < 32; i <<= 1) {
        x ^= x >> i;
    }
    return x;
}

uint32_t invert_xor_left(uint32_t y, unsigned shift) {
    uint32_t x = y;
    for (unsigned i = shift; i < 32; i <<= 1) {
        x ^= x << i;
    }
    return x;
}

uint32_t kinda_random_stuff2(uint32_t value) {
    uint32_t x = value;
    x ^= x << 7;
    x ^= x >> 3;
    x ^= x << 5;
    x ^= x >> 2;
    x ^= 0xA5C3F1E9u;
    return x;
}

uint32_t invert_kinda_random_stuff2(uint32_t value) {
    uint32_t x = value ^ 0xA5C3F1E9u;
    x = invert_xor_right(x, 2);
    x = invert_xor_left(x, 5);
    x = invert_xor_right(x, 3);
    x = invert_xor_left(x, 7);
    return x;
}

