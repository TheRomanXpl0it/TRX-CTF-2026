uint32_t vm_rand_next(VM *vm) {
    uint32_t x = vm->rng_state;
    x ^= x << 13;
    x ^= x >> 17;
    x ^= x << 5;
    vm->rng_state = x;
    return x;
}

void vm_push_imm(VM *vm, uint32_t arg) {
    vm_push(vm, arg);
}

void vm_store_mem(VM *vm, uint32_t arg) {
    if (vm->sp >= 0) {
        vm->mem[arg] = vm->stack[vm->sp--];
    }
}

void vm_load_mem(VM *vm, uint32_t arg) {
    vm_push(vm, vm->mem[arg]);
}

void vm_jump(VM *vm, uint32_t arg) {
    vm->ip = arg;
}

void vm_jz(VM *vm, uint32_t arg) {
    if (vm->sp >= 0) {
        int32_t cond = vm->stack[vm->sp--];
        if (cond == 0) vm->ip = arg;
    }
}

void vm_jnz(VM *vm, uint32_t arg) {
    if (vm->sp >= 0) {
        int32_t cond = vm->stack[vm->sp--];
        if (cond != 0) vm->ip = arg;
    }
}

void vm_call_op(VM *vm, uint32_t arg) {
    vm_push(vm, vm->ip);
    vm->ip = arg;
}

void vm_ret_op(VM *vm) {
    if (vm->sp >= 0) {
        vm->ip = vm->stack[vm->sp--];
    }
}

void vm_eq(VM *vm) {
    if (vm->sp >= 1) {
        int32_t b = vm->stack[vm->sp--];
        int32_t a = vm->stack[vm->sp--];
        vm_push(vm, a == b);
    }
}

void vm_lt(VM *vm) {
    if (vm->sp >= 1) {
        int32_t b = vm->stack[vm->sp--];
        int32_t a = vm->stack[vm->sp--];
        vm_push(vm, a < b);
    }
}

void vm_gt(VM *vm) {
    if (vm->sp >= 1) {
        int32_t b = vm->stack[vm->sp--];
        int32_t a = vm->stack[vm->sp--];
        vm_push(vm, a > b);
    }
}

void vm_neg(VM *vm) {
    if (vm->sp >= 0) {
        int32_t a = vm->stack[vm->sp];
        if (a < 0) vm->stack[vm->sp] = -a;
    }
}

void vm_xor(VM *vm) {
    if (vm->sp >= 1) {
        int32_t b = vm->stack[vm->sp--];
        int32_t a = vm->stack[vm->sp--];
        vm_push(vm, a ^ b);
    }
}

void vm_halt(VM *vm) {
    vm->ip = vm->code_size;
}

void vm_rand(VM *vm, uint32_t arg) {
    uint32_t r = vm_rand_next(vm);
    vm_push(vm, arg ? (int32_t)(r % arg) : (int32_t)r);
}

void vm_read_int(VM *vm) {
    int row, col;
    char sep;
    if (scanf("%d%c%d", &row, &sep, &col) == 3) {
        vm_push(vm, col);
        vm_push(vm, row);
    }
}

void vm_load_ind(VM *vm) {
    if (vm->sp >= 0) {
        uint32_t addr = vm->stack[vm->sp--];
        vm_push(vm, vm->mem[addr]);
    }
}

void vm_store_ind(VM *vm) {
    if (vm->sp >= 1) {
        uint32_t addr = vm->stack[vm->sp--];
        uint32_t value = vm->stack[vm->sp--];
        vm->mem[addr] = value;
    }
}

