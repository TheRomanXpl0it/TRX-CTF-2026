void vm_run(VM *vm) {
    while (vm->ip < vm->code_size) {
        Instr ins = vm->code[vm->ip++];
        uint32_t op  = kinda_random_stuff1(ins.op);
        uint32_t arg = invert_kinda_random_stuff2(ins.arg);

        switch (op) {
            case 0x02u:
                vm_push_imm(vm, arg);
                break;

            case 0x05u:
                vm_add(vm);
                break;

            case 0x06u:
                vm_sub(vm);
                break;

            case 0x07u:
                vm_mul(vm);
                break;

            case 0x08u:
                vm_div(vm);
                break;

            case 0x1Du:
                vm_load_mem(vm, arg);
                break;

            case 0x0Bu:
                vm_store_mem(vm, arg);
                break;

            case 0x0Cu:
                vm_jump(vm, arg);
                break;

            case 0x0Du:
                vm_jz(vm, arg);
                break;

            case 0x1Eu:
                vm_jnz(vm, arg);
                break;

            case 0x0Fu:
                vm_call_op(vm, arg);
                break;

            case 0x22u:
                vm_ret_op(vm);
                break;

            case 0x11u:
                vm_eq(vm);
                break;

            case 0x26u:
                vm_lt(vm);
                break;

            case 0x13u:
                vm_gt(vm);
                break;

            case 0x27u:
                vm_neg(vm);
                break;

            case 0x28u:
                vm_xor(vm);
                break;

            case 0x18u:
                vm_halt(vm);
                break;

            case 0x19u:
                vm_read_int(vm);
                break;

            case 0x1Au:
                vm_load_ind(vm);
                break;

            case 0x1Bu:
                vm_store_ind(vm);
                break;

            case 0x29u:
                vm_print_ok(vm);
                break;

            case 0x2Au:
                vm_rand(vm, arg);
                break;

            case 0x21u:
                vm_print_err(vm);
                break;

            default:
                vm_halt(vm);
                break;
        }
    }
}

Instr* load_embedded_bytecode(size_t *count) {
    Instr *code = NULL;
    size_t instr_count = 0;

    if ((bytecode_len % sizeof(Instr)) != 0) {
        return NULL;
    }

    instr_count = (size_t)(bytecode_len / sizeof(Instr));
    code = malloc(instr_count * sizeof(Instr));
    if (!code) return NULL;

    memcpy(code, bytecode, bytecode_len);
    *count = instr_count;
    return code;
}

int main(int argc, char **argv) {
    (void)argc;
    (void)argv;
    setvbuf(stdout, NULL, _IONBF, 0);
    size_t count;
    Instr *code = load_embedded_bytecode(&count);
    if (!code) {
        return 1;
    }

    VM *vm = vm_create(code, count);
    if (!vm) {
        free(code);
        printf("Failed initialization\n");
        return 1;
    }

    vm_run(vm);

    vm_free(vm);
    free(code);

    return 0;
}
